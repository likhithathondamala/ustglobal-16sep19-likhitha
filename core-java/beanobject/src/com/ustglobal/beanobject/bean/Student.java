package com.ustglobal.beanobject.bean;

public class Student {
	private int id;
	private String name;
	private int mobno;
	public Student() {

	}
	public void setId(int id) {
		this.id = id;

	}
	public int getId() {
		return id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setmobno(int mobno) {
		this.mobno = mobno;

	}
	public int getmobno() {
		return mobno;
	}
}
